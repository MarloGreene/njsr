function generateRandomUUID() {
    const array = new Uint8Array(16);
    crypto.getRandomValues(array);
    // Set version (4) and variant bits
    array[6] = (array[6] & 0x0f) | 0x40;
    array[8] = (array[8] & 0x3f) | 0x80;
    const hex = Array.from(array, byte => byte.toString(16).padStart(2, '0')).join('');
    return `${hex.slice(0, 8)}-${hex.slice(8, 12)}-${hex.slice(12, 16)}-${hex.slice(16, 20)}-${hex.slice(20, 32)}`;
}

function updateDisplays() {
    document.getElementById('random-number').textContent = generateRandomNumber();
    const passLength = parseInt(document.getElementById('password-length').value) || 12;
    document.getElementById('random-password').textContent = generateRandomPassword(passLength);
    const phraseWords = parseInt(document.getElementById('passphrase-words').value) || 4;
    document.getElementById('random-passphrase').textContent = generateRandomPassphrase(phraseWords);
    document.getElementById('random-uuid').textContent = generateRandomUUID();
}

async function copyToClipboard(text) {
    try {
        await navigator.clipboard.writeText(text);
        showToast();
    } catch (err) {
        console.error('Failed to copy: ', err);
        // Fallback for older browsers
        const textArea = document.createElement('textarea');
        textArea.value = text;
        document.body.appendChild(textArea);
        textArea.select();
        document.execCommand('copy');
        document.body.removeChild(textArea);
        showToast();
    }
}

function showToast() {
    const toast = document.getElementById('toast');
    toast.classList.add('show');
    setTimeout(() => {
        toast.classList.remove('show');
    }, 2000);
}

function updateTimestamp() {
    const now = new Date();
    const humanTime = now.toLocaleTimeString('en-US', { hour12: false });
    const unixTime = Math.floor(now.getTime() / 1000);
    document.getElementById('human-time').textContent = humanTime;
    document.getElementById('unix-time').textContent = unixTime;
}

